package com.suneee.kaoqin.model.kaoqin;

/**
 * 页面展示用model
 * @author mikel
 *
 */
public class ViewModel {

	private String value;
	private int type;
	private int state;
	
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
}
